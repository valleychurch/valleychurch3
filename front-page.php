      <p>Perry Noble often says that every number has a name, every name has a story and every story matters to God. Which is why we count numbers, and which is why we want to <em>get</em> numbers! We want to get phone numbers, email addresses and as much information about the people of Valley Church as possible. Why is that? Here are eight reasons:</p>
      <ol>
      <li><strong>To care for people.</strong> Without a phone number, how can we follow up on a prayer request, a difficult situation or someone who’s not able to attend due to sickness?</li>
      <li><strong>To invite people.</strong> People don’t serve events, events serve people. And so how can we get the right people to the right things if we can’t invite them personally?</li>
      <li><strong>To show that we’re interested.</strong> Every single human being is a unique image-bearer of God. That’s something we should be interested in! Being interested in people means committing to connect with them, and that usually requires a phone number!</li>
      <li><strong>To provide opportunity for ownership.</strong> Becoming an integral part of a community involves being able to get in touch. The body of Christ is something people <em>need</em> to be a part of and taking the responsibility to give your details moves you from spectator to participant.</li>
      <li><strong>To help people take their best next step.</strong> Knowing where people are at involves talking to them, getting to know them and finding out information about them. Then we can serve every unique individual by responding faithfully to the requests they have logged on a details card.</li>
      <li><strong>To facilitate personal invitation.</strong> A general invite is never as good as a personal one. Most people start off in church life being dragged along. Let’s faithfully and patiently come alongside people on an individual basis and take the time to help them connect to real community.</li>
      <li><strong>To give the church permission.</strong> When someone gives in their details, the church has permission to contact them in a non-weird way. We have been welcomed in to a life that God values so much, and now we have opportunity and permission to serve that life.</li>
      <li><strong>To ensure accountability.</strong> Shepherding the flock, and knowing the flock are two things God takes seriously. Ensuring that every individual has the <em>best possible opportunity</em> to connect to the church family requires a system of accountability, hence, details cards.</li>
      </ol>
